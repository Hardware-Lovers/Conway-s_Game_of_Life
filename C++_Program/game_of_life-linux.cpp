void updtgrid(int grid1[N][M], int grid2[N][M])
{
	for(int i=0;i<N;i++)
		for(int j=0;j<M;j++)
			grid1[i][j]=grid2[i][j];
			
	int alive[N][M]={};	
	for(int i=0;i<N;i++)
		for(int j=0;j<M;j++)
				for(int k=-1;k<=1;k++)
					for(int l=-1;l<=1;l++)
						if(!(i+k<0||i+k>=N||j+l<0||j+l>=M||(k==0&&l==0)))
							if(grid1[i+k][j+l]==1)alive[i][j]++;
	for(int i=0;i<N;i++)
		for(int j=0;j<M;j++)
			{
				if(alive[i][j]<2)grid2[i][j]=0;
				else if(alive[i][j]==2)grid2[i][j]=grid1[i][j];
				else if(alive[i][j]==3)grid2[i][j]=1;
				else grid2[i][j]=0;
			}
}
